#!/usr/bin/env node
/* eslint-disable */
/**
 * Minimal Rush launcher for the dreamux monorepo.
 *
 * Usage:
 *   node common/scripts/install-run-rush.js <rush-subcommand> [args...]
 *
 * This is a deliberately small wrapper: it reads `rushVersion` from
 * `rush.json` and shells out to `npx @microsoft/rush@<version>`. That keeps
 * the bootstrap zero-config — any Node install ships npx — at the cost of
 * letting npm cache the rush install in $HOME/.npm/_npx/ on first run.
 *
 * If reproducible offline bootstrap becomes important, replace this with
 * the full `install-run-rush.js` + `install-run.js` pair generated by
 * `rush init` (~400 LoC of standard rushstack boilerplate). For MVP this
 * wrapper is enough; see .agents/decisions/0001-rush-pnpm-monorepo.md
 * for the trade-off.
 */
'use strict';

const { spawnSync } = require('node:child_process');
const { readFileSync } = require('node:fs');
const { dirname, join } = require('node:path');

function readRushJson() {
  // rush.json may contain // and /* */ comments — JSON-with-comments is
  // the rush convention. Strip them before parsing.
  const path = join(__dirname, '..', '..', 'rush.json');
  const raw = readFileSync(path, 'utf8');
  const stripped = raw
    .replace(/\/\*[\s\S]*?\*\//g, '')
    .replace(/(^|\s)\/\/[^\n]*/g, '$1');
  return JSON.parse(stripped);
}

function main() {
  const args = process.argv.slice(2);
  if (args.length === 0) {
    console.error(
      'install-run-rush.js: missing rush subcommand (try `update`, `build`, `test`, `--help`)',
    );
    process.exit(2);
  }
  const { rushVersion } = readRushJson();
  if (typeof rushVersion !== 'string' || rushVersion.length === 0) {
    console.error('install-run-rush.js: rush.json is missing `rushVersion`');
    process.exit(2);
  }
  const repoRoot = dirname(dirname(__dirname));
  const result = spawnSync(
    'npx',
    ['-y', `@microsoft/rush@${rushVersion}`, ...args],
    { stdio: 'inherit', cwd: repoRoot },
  );
  if (result.error) {
    console.error('install-run-rush.js: failed to invoke npx:', result.error);
    process.exit(1);
  }
  process.exit(result.status ?? 1);
}

main();
